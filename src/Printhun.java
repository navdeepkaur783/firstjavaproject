public class Printhun
{
    public static void main(String args[])
    {
        int i;
        System.out.println("Numbers from 1 to 100 are as follows");
        for (i=1;i<=100;i++)
        {
            System.out.println(i);
        }
        System.out.println("Numbers from 100 to 1 are as follows");
        for (i=100; i>=1;i--)
        {
            System.out.println(i);
        }
    }
}
